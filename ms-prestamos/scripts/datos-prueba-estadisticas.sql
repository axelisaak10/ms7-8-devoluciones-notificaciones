-- Verificaciones rápidas equivalentes a las estadísticas.
SELECT COUNT(*) AS prestamos_ultimos_30_dias
FROM operaciones.prestamos
WHERE fecha_prestamo >= CURRENT_DATE - INTERVAL '30 days'
  AND fecha_prestamo < CURRENT_DATE + INTERVAL '1 day';

SELECT l.titulo, COUNT(*) AS total_prestamos
FROM operaciones.prestamos p
JOIN catalogo.libros l ON l.id = p.libro_id
GROUP BY l.id, l.titulo
ORDER BY total_prestamos DESC;

SELECT u.nombre_completo, u.email, COUNT(*) AS total_sanciones
FROM operaciones.sanciones s
JOIN identidad.usuarios u ON u.id = s.usuario_id
GROUP BY u.id, u.nombre_completo, u.email
ORDER BY total_sanciones DESC;
